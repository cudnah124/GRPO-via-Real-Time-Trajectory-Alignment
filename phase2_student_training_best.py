val_labels = val_data['labels']

test_model.eval()

# Chọn 5 mẫu đại diện có các nhãn khác nhau (khớp tốt, lệch vừa, lệch nhiều)
# Tìm các index thỏa mãn điều kiện nhãn để hiển thị trực quan
indices_to_test = []
labels_np = val_labels.numpy()

# 1. Tìm mẫu khớp hoàn hảo (0.0)
idx_0 = np.where(labels_np == 0.0)[0]
if len(idx_0) > 0: indices_to_test.append(random.choice(idx_0))

# 2. Tìm mẫu lệch nhẹ (0.05)
idx_05 = np.where((labels_np > 0.0) & (labels_np <= 0.1))[0]
if len(idx_05) > 0: indices_to_test.append(random.choice(idx_05))

# 3. Tìm mẫu lệch vừa (0.4)
idx_40 = np.where((labels_np > 0.3) & (labels_np <= 0.5))[0]
if len(idx_40) > 0: indices_to_test.append(random.choice(idx_40))

# 4. Tìm mẫu lệch nặng (0.65)
idx_65 = np.where((labels_np > 0.5) & (labels_np < 1.0))[0]
if len(idx_65) > 0: indices_to_test.append(random.choice(idx_65))

# 5. Tìm mẫu lạc đề chéo bài toán (1.0)
idx_100 = np.where(labels_np == 1.0)[0]
if len(idx_100) > 0: indices_to_test.append(random.choice(idx_100))

# Nếu thiếu mẫu nào thì điền ngẫu nhiên cho đủ 5 mẫu
while len(indices_to_test) < 5:
    indices_to_test.append(random.randint(0, len(val_labels) - 1))

print("=== KIỂM THỬ TRÊN TẬP VALIDATION THỰC TẾ ===")

for rank, idx in enumerate(indices_to_test):
    # Lấy tensor đặc trưng của mẫu
    ia = val_ids_a[idx:idx+1].to(device)
    ma = val_mask_a[idx:idx+1].to(device)
    ib = val_ids_b[idx:idx+1].to(device)
    mb = val_mask_b[idx:idx+1].to(device)
    gt_label = val_labels[idx].item()

    # Dự đoán bằng Student Model
    with torch.no_grad():
        pred_cost = test_model(ia, ma, ib, mb).item()

    # Giải mã văn bản để đọc
    # Loại bỏ các token pad và special tokens để dễ nhìn
    text_a = test_tokenizer.decode(val_ids_a[idx], skip_special_tokens=True).strip()
    text_b = test_tokenizer.decode(val_ids_b[idx], skip_special_tokens=True).strip()

    # Rút ngắn hiển thị nếu text quá dài
    def short_text(text, max_len=150):
        return text[:max_len] + "..." if len(text) > max_len else text

    print(f"\n[Mẫu Test {rank+1}] (Chỉ số dòng: {idx})")
    print(f"  Văn bản A : \"{short_text(text_a)}\"")
    print(f"  Văn bản B : \"{short_text(text_b)}\"")
    print(f"  ==> Nhãn gốc Teacher (GT Label): {gt_label:.4f}")
    print(f"  ==> Dự đoán Student (Pred Cost): {pred_cost:.4f}")