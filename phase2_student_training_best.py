class LogicalAlignmentStudent(nn.Module):
    def __init__(self, model_id="witiko/mathberta", freeze_backbone=False):
        super().__init__()
        self.backbone = AutoModel.from_pretrained(model_id)

        # Kiến trúc ESIM: Kính lúp MLP nhận 4 luồng đặc trưng (A, B_aligned, A-B_aligned, A*B_aligned)
        hidden = self.backbone.config.hidden_size
        self.comparator = nn.Sequential(
            nn.Linear(hidden * 4, 128),
            nn.ReLU(),
            nn.Linear(128, 1),
            nn.Sigmoid() # Ép nhãn dự đoán trong khoảng [0, 1] tuyệt đối
        )

        if freeze_backbone:
            for param in self.backbone.parameters():
                param.requires_grad = False
            try:
                for layer in self.backbone.encoder.layer[-2:]:
                    for param in layer.parameters():
                        param.requires_grad = True
                print('[*] Đã freeze backbone và chỉ unfreeze 2 layer cuối của encoder.')
            except Exception as e:
                print(f'[!] Không thể selective unfreeze: {e}')

    def forward(self, ids_a, mask_a, ids_b, mask_b):
        emb_a = self.backbone(ids_a, mask_a).last_hidden_state # (B, L_a, H)
        emb_b = self.backbone(ids_b, mask_b).last_hidden_state # (B, L_b, H)

        # 1. Softmax Alignment (Từ B sang A)
        raw_attn_scores = torch.bmm(emb_a, emb_b.transpose(1, 2)) / math.sqrt(emb_a.size(-1)) # (B, L_a, L_b)
        mask_b_exp = mask_b.unsqueeze(1).expand(-1, emb_a.size(1), -1)
        attn_scores = raw_attn_scores.masked_fill(mask_b_exp == 0, -1e4)
        attn_weights = F.softmax(attn_scores, dim=-1) # (B, L_a, L_b)
        aligned_b = torch.bmm(attn_weights, emb_b) # (B, L_a, H)

        # 2. Extract Contradiction Features & Predict d_t cho câu A
        diff_a = emb_a - aligned_b
        mul_a = emb_a * aligned_b
        m_a = torch.cat([emb_a, aligned_b, diff_a, mul_a], dim=-1) # (B, L_a, 4H)
        d_t_a = self.comparator(m_a).squeeze(-1) # (B, L_a) trong khoảng [0, 1]

        # 3. Softmax Alignment (Từ A sang B)
        mask_a_exp = mask_a.unsqueeze(1).expand(-1, emb_b.size(1), -1)
        attn_scores_rev = raw_attn_scores.transpose(1, 2) # (B, L_b, L_a)
        attn_scores_rev = attn_scores_rev.masked_fill(mask_a_exp == 0, -1e4)
        attn_weights_rev = F.softmax(attn_scores_rev, dim=-1) # (B, L_b, L_a)
        aligned_a = torch.bmm(attn_weights_rev, emb_a) # (B, L_b, H)

        # 4. Extract Contradiction Features & Predict d_t cho câu B
        diff_b = emb_b - aligned_a
        mul_b = emb_b * aligned_a
        m_b = torch.cat([emb_b, aligned_a, diff_b, mul_b], dim=-1) # (B, L_b, 4H)
        d_t_b = self.comparator(m_b).squeeze(-1) # (B, L_b) trong khoảng [0, 1]

        # 5. Dynamic Top-K Pooling cho từng câu (Dựa trên độ dài thực tế, tính per-sample)
        real_len_a = mask_a.sum(dim=1).float()
        K_a = torch.clamp((0.05 * real_len_a).long(), min=3).unsqueeze(1)
        d_t_a_masked = d_t_a.masked_fill(mask_a == 0, 0.0)
        sorted_a, _ = torch.sort(d_t_a_masked, dim=1, descending=True)
        ranks_a = torch.arange(d_t_a_masked.size(1), device=d_t_a_masked.device).unsqueeze(0).expand(d_t_a_masked.size(0), -1)
        cost_a = (sorted_a * (ranks_a < K_a)).sum(dim=1) / K_a.squeeze(1).float()

        real_len_b = mask_b.sum(dim=1).float()
        K_b = torch.clamp((0.05 * real_len_b).long(), min=3).unsqueeze(1)
        d_t_b_masked = d_t_b.masked_fill(mask_b == 0, 0.0)
        sorted_b, _ = torch.sort(d_t_b_masked, dim=1, descending=True)
        ranks_b = torch.arange(d_t_b_masked.size(1), device=d_t_b_masked.device).unsqueeze(0).expand(d_t_b_masked.size(0), -1)
        cost_b = (sorted_b * (ranks_b < K_b)).sum(dim=1) / K_b.squeeze(1).float()

        # 6. Aggregation
        dist = 0.5 * cost_a + 0.5 * cost_b
        return dist

# 4. TripletDataset: Nhóm các cặp (A, B) thành triplet (A, B_pos, B_neg)
import random
from torch.utils.data import TensorDataset

class TripletDataset(Dataset):
    """
    Xây dựng triplets từ dataset gốc:
    - Positive (B_pos): các cặp có label <= pos_threshold (khớp tốt, chi phí thấp)
    - Negative (B_neg): các cặp có label >= neg_threshold (không khớp, chi phí cao)
    Mỗi triplet = (A từ positive pair, B_pos, B_neg)
    """
    def __init__(self, ids_a, mask_a, ids_b, mask_b, labels,
                 pos_threshold=0.10, neg_threshold=0.30):
        self.ids_a  = ids_a
        self.mask_a = mask_a
        self.ids_b  = ids_b
        self.mask_b = mask_b
        self.labels = labels
        self.pos_threshold = pos_threshold
        self.neg_threshold = neg_threshold

        import hashlib
        # Nhóm index theo anchor (ids_a) để bảo đảm A cố định cho mỗi triplet
        self.anchor_to_idx = {}
        for i in range(len(labels)):
            h = hashlib.md5(ids_a[i].numpy().tobytes()).hexdigest()
            if h not in self.anchor_to_idx:
                self.anchor_to_idx[h] = []
            self.anchor_to_idx[h].append(i)

        self._build_triplets()

    def _build_triplets(self):
        self.triplets = []
        for indices in self.anchor_to_idx.values():
            pos_indices = [i for i in indices if self.labels[i].item() <= self.pos_threshold]
            neg_indices = [i for i in indices if self.labels[i].item() >= self.neg_threshold]

            for p in pos_indices:
                valid_neg_idx = []
                cost_pos = self.labels[p].item()
                if cost_pos > 0.05:
                    valid_neg_idx = [n for n in neg_indices if self.labels[n].item() >= 0.45]
                else:
                    valid_neg_idx = neg_indices

                # Bỏ global fallback để bảo đảm A hoàn toàn cố định
                if len(valid_neg_idx) > 0:
                    n = random.choice(valid_neg_idx)
                    self.triplets.append((p, p, n))

        print(f'[+] Đã rebuild xong {len(self.triplets)} triplets.')

    def __len__(self):
        return len(self.triplets)

    def __getitem__(self, idx):
        a_i, p_i, n_i = self.triplets[idx]
        return (
            self.ids_a[a_i],  self.mask_a[a_i],   # Anchor A
            self.ids_b[p_i],  self.mask_b[p_i],   # B_positive
            self.labels[p_i],                       # Label positive
            self.ids_b[n_i],  self.mask_b[n_i],   # B_negative
            self.labels[n_i],                       # Label negative
        )

