                    "        cost_b = (sorted_b * (ranks_b < K_b)).sum(dim=1) / K_b.squeeze(1).float()\n",
                    "        \n",
                    "        # 6. Aggregation\n",
                    "        dist = 0.5 * cost_a + 0.5 * cost_b\n",
                    "        return dist"
