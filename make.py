                    "    \"\"\"Tính chi phí đường đi ngắn nhất (Alignment Cost) từ ma trận 0/1 của Teacher\"\"\"\n",
                    "    N, M = len(matrix), len(matrix[0])\n",
                    "    dp = [[float('inf')] * (M + 1) for _ in range(N + 1)]\n",
