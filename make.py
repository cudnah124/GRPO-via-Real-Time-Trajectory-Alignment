                    "class LogicalAlignmentStudent(nn.Module):\n",
                    "    def __init__(self, model_id=\"witiko/mathberta\", freeze_backbone=False):\n",
                    "        super().__init__()\n",
                    "        self.backbone = AutoModel.from_pretrained(model_id)\n",
                    "        \n",
                    "        # Kiến trúc ESIM: Kính lúp MLP nhận 4 luồng đặc trưng (A, B_aligned, A-B_aligned, A*B_aligned)\n",
                    "        hidden = self.backbone.config.hidden_size\n",
                    "        # [MỚI] Fix MSE Gradient Blocking: Dùng Softplus thay cho ReLU ở layer cuối\n",
                    "        self.comparator = nn.Sequential(\n",
                    "            nn.Linear(hidden * 4, 128),\n",
                    "            nn.ReLU(),\n",
                    "            nn.Linear(128, 1),\n",
                    "            nn.Softplus(beta=10) # Tránh gradient=0 khi pred=0 mà target > 0\n",
                    "        )\n",
                    "        \n",
                    "        if freeze_backbone:\n",
                    "            for param in self.backbone.parameters():\n",
                    "                param.requires_grad = False\n",
                    "            try:\n",
                    "                for layer in self.backbone.encoder.layer[-2:]:\n",
                    "                    for param in layer.parameters():\n",
                    "                        param.requires_grad = True\n",
                    "                print('[*] Đã freeze backbone và chỉ unfreeze 2 layer cuối của encoder.')\n",
                    "            except Exception as e:\n",
                    "                print(f'[!] Không thể selective unfreeze: {e}')\n",
                    "\n",
                    "    def forward(self, ids_a, mask_a, ids_b, mask_b):\n",
                    "        emb_a = self.backbone(ids_a, mask_a).last_hidden_state # (B, L_a, H)\n",
                    "        emb_b = self.backbone(ids_b, mask_b).last_hidden_state # (B, L_b, H)\n",
                    "        \n",
                    "        # 1. Softmax Alignment (Từ B sang A)\n",
                    "        raw_attn_scores = torch.bmm(emb_a, emb_b.transpose(1, 2)) / math.sqrt(emb_a.size(-1)) # (B, L_a, L_b)\n",
                    "        mask_b_exp = mask_b.unsqueeze(1).expand(-1, emb_a.size(1), -1)\n",
                    "        attn_scores = raw_attn_scores.masked_fill(mask_b_exp == 0, -1e4)\n",
                    "        attn_weights = F.softmax(attn_scores, dim=-1) # (B, L_a, L_b)\n",
                    "        aligned_b = torch.bmm(attn_weights, emb_b) # (B, L_a, H)\n",
                    "        \n",
                    "        # 2. Extract Contradiction Features & Predict d_t cho câu A\n",
                    "        diff_a = emb_a - aligned_b\n",
                    "        mul_a = emb_a * aligned_b\n",
                    "        m_a = torch.cat([emb_a, aligned_b, diff_a, mul_a], dim=-1) # (B, L_a, 4H)\n",
                    "        d_t_a = self.comparator(m_a).squeeze(-1) # (B, L_a) trong khoảng [0, 1]\n",
                    "        \n",
                    "        # 3. Softmax Alignment (Từ A sang B)\n",
                    "        mask_a_exp = mask_a.unsqueeze(1).expand(-1, emb_b.size(1), -1)\n",
                    "        attn_scores_rev = raw_attn_scores.transpose(1, 2) # (B, L_b, L_a) \n",
                    "        attn_scores_rev = attn_scores_rev.masked_fill(mask_a_exp == 0, -1e4)\n",
                    "        attn_weights_rev = F.softmax(attn_scores_rev, dim=-1) # (B, L_b, L_a)\n",
                    "        aligned_a = torch.bmm(attn_weights_rev, emb_a) # (B, L_b, H)\n",
                    "        \n",
                    "        # 4. Extract Contradiction Features & Predict d_t cho câu B\n",
                    "        diff_b = emb_b - aligned_a\n",
                    "        mul_b = emb_b * aligned_a\n",
                    "        m_b = torch.cat([emb_b, aligned_a, diff_b, mul_b], dim=-1) # (B, L_b, 4H)\n",
                    "        d_t_b = self.comparator(m_b).squeeze(-1) # (B, L_b) trong khoảng [0, 1]\n",
                    "        \n",
                    "        # 5. Dynamic Top-K Pooling cho từng câu (Dựa trên độ dài thực tế, tính per-sample)\n",
                    "        real_len_a = mask_a.sum(dim=1).float()\n",
                    "        K_a = torch.clamp((0.05 * real_len_a).long(), min=3).unsqueeze(1)\n",
                    "        d_t_a_masked = d_t_a.masked_fill(mask_a == 0, 0.0)\n",
                    "        sorted_a, _ = torch.sort(d_t_a_masked, dim=1, descending=True)\n",
                    "        ranks_a = torch.arange(d_t_a_masked.size(1), device=d_t_a_masked.device).unsqueeze(0).expand(d_t_a_masked.size(0), -1)\n",
                    "        cost_a = (sorted_a * (ranks_a < K_a)).sum(dim=1) / K_a.squeeze(1).float()\n",
                    "        \n",
                    "        real_len_b = mask_b.sum(dim=1).float()\n",
                    "        K_b = torch.clamp((0.05 * real_len_b).long(), min=3).unsqueeze(1)\n",
                    "        d_t_b_masked = d_t_b.masked_fill(mask_b == 0, 0.0)\n",
                    "        sorted_b, _ = torch.sort(d_t_b_masked, dim=1, descending=True)\n",
